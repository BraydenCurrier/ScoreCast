def draw_4x5_char(draw, x, y, char, color):
    if char not in FONT_4x5_DATA:
        return

    for row in range(5):
        row_data = FONT_4x5_DATA[char][row]
        for col in range(4):
            if (row_data >> (3 - col)) & 1:
                draw.point((x + col, y + row), fill=color)

def print_4x5(draw, text, x, y, color):
    current_x = x
    text = str(text).upper()

    for char in text:
        if char == ' ':
            current_x += 3
        elif char == ':':
            draw_4x5_char(draw, current_x - 1, y, char, color)
            current_x += 2  # 1px width + 1px gap
        elif char == '1':
            # Shift back by 1 pixel to strip the blank column on the left
            draw_4x5_char(draw, current_x, y, char, color)
            current_x += 4  # 3px actual width + 1px trailing character gap
        elif char in FONT_4x5_DATA:
            draw_4x5_char(draw, current_x, y, char, color)
            current_x += 5  # 4px width + 1px gap
        else:
            current_x += 2

def get_4x5_width(text):
    total_width = 0
    text = str(text).upper()
    
    for char in text:
        if char == ' ':
            total_width += 3
        elif char == ':':
            total_width += 2
        elif char == '1':
            total_width += 4  # 3px actual width + 1px trailing character gap
        elif char in FONT_4x5_DATA:
            total_width += 5
        else:
            total_width += 2
            
    return total_width - 1 if total_width > 0 else 0

def print_4x5_centered(draw, text, center_x, y, color):
    text_str = str(text)
    
    string_width = get_4x5_width(text_str)
    
    start_x = center_x - (string_width // 2)
    
    print_4x5(draw, text_str, start_x, y, color)

def print_4x5_right(draw, text, right_x, y, color):
    text = str(text)

    width = get_4x5_width(text)

    print_4x5(
        draw,
        text,
        right_x - width,
        y,
        color
    )

def print_clock(draw, clock_string, center_x, y, color):
    
    clock_str = str(clock_string).strip().upper()
    
    if ':' not in clock_str:
        print_4x5_centered(draw, clock_str, center_x, y, color)
        return

    time_parts = clock_str.split(':')
    minutes_str = time_parts[0]
    seconds_str = time_parts[1]
    
    if len(seconds_str) == 1:
        seconds_str = "0" + seconds_str

    colon_start_x = center_x - 2
    print_4x5(draw, ":", colon_start_x, y, color)
    
    minutes_width = get_4x5_width(minutes_str)

    minutes_start_x = colon_start_x - minutes_width - 1
    print_4x5(draw, minutes_str, minutes_start_x, y, color)
    
    seconds_start_x = colon_start_x + 2
    print_4x5(draw, seconds_str, seconds_start_x, y, color)

def draw_3x5_glyph(draw, x, y, index, color):
    if index < 0 or index > 12:
        return

    for row in range(5):
        row_data = MICRO_FONT[index][row]
        for col in range(3):
            if (row_data >> (2 - col)) & 1:
                draw.point((x + col, y + row), fill=color)

def print_3x5(draw, text, x, y, color):
    current_x = x

    for c in str(text):
        if c.isdigit():
            draw_3x5_glyph(draw, current_x, y, int(c), color)
            current_x += 4
        elif c == "-":
            draw_3x5_glyph(draw, current_x, y, 10, color)
            current_x += 4
        elif c == ":":
            draw_3x5_glyph(draw, current_x, y, 11, color)
            current_x += 4
        elif c == "#":
            draw_3x5_glyph(draw, current_x, y, 12, color)
            current_x += 4
        elif c == " ":
            current_x += 3

def get_3x5_width(text):
    total = 0

    for c in str(text):
        if c.isdigit() or c in "-:#":
            total += 4
        elif c == " ":
            total += 3

    return total - 1 if total > 0 else 0

def print_3x5_right(draw, text, right_x, y, color):
    text = str(text)

    width = get_3x5_width(text)

    print_3x5(
        draw,
        text,
        right_x - width,
        y,
        color
    )

def print_gfx_5x7(draw, text, x, y, color):
    cursor_x = x

    for char in str(text).upper():
        bitmap = GFX_5X7.get(char, GFX_5X7[" "])

        for col in range(5):
            column_data = bitmap[col]

            for row in range(7):
                if (column_data >> row) & 1:
                    draw.point((cursor_x + col, y + row), fill=color)

        cursor_x += 6

def gfx_5x7_width(text):
    return len(str(text)) * 6 - 1

def draw_text_right(draw, text, right_x, y, color):
    text = str(text)

    width = gfx_5x7_width(text)

    print_gfx_5x7(
        draw,
        text,
        right_x - width,
        y,
        color
    )